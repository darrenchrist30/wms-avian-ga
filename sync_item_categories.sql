JOIN tmp_item_category_sync s ON s.sku = i.sku
JOIN item_categories c ON c.code = s.category_code AND c.deleted_at IS NULL
SET i.category_id = c.id,
    i.updated_at = NOW()
WHERE i.deleted_at IS NULL;

